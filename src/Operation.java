public class Operation {
    public int sum(int x,int y){
        int z=x+y;
        return z;
    }
    public int mult(int x,int y){
        int z=x*y;
        return z;
    }
    public int subt(int x,int y){
        int z=x-y;
        return z;
    }
    public int div(int x,int y){
        int z=x/y;
        return z;
    }
}
